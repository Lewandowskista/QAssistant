        /// <summary>
        /// Generate a Markdown bug report from a failed test case, enriched with
        /// execution history and the latest AI analysis entry.
        /// </summary>
        public static string GenerateFromTestCase(
            TestCase tc,
            string testPlanName,
            string environment,
            string reporter,
            IEnumerable<TestExecution>? executions,
            string? aiAnalysis = null)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"# Bug Report: {tc.Title}");
            sb.AppendLine();
            sb.AppendLine("## Summary");
            sb.AppendLine($"> {tc.Title}");
            sb.AppendLine();

            sb.AppendLine("## Reference");
            sb.AppendLine($"- **Test Case ID:** {tc.TestCaseId}");
            if (!string.IsNullOrWhiteSpace(testPlanName))
                sb.AppendLine($"- **Test Plan:** {testPlanName}");
            if (!string.IsNullOrWhiteSpace(tc.SourceIssueId))
                sb.AppendLine($"- **Source Issue:** {tc.SourceIssueId}");
            sb.AppendLine();

            sb.AppendLine("## Environment");
            sb.AppendLine($"- **Environment:** {(string.IsNullOrWhiteSpace(environment) ? "Not specified" : environment)}");
            sb.AppendLine($"- **Date Reported:** {DateTime.Now:yyyy-MM-dd HH:mm}");
            if (!string.IsNullOrWhiteSpace(reporter))
                sb.AppendLine($"- **Reporter:** {reporter}");
            sb.AppendLine();

            sb.AppendLine("## Priority & Severity");
            sb.AppendLine($"- **Priority:** {tc.Priority}");
            sb.AppendLine();

            if (!string.IsNullOrWhiteSpace(tc.PreConditions))
            {
                sb.AppendLine("## Pre-Conditions");
                sb.AppendLine(tc.PreConditions);
                sb.AppendLine();
            }

            if (!string.IsNullOrWhiteSpace(tc.TestSteps))
            {
                sb.AppendLine("## Steps to Reproduce");
                sb.AppendLine(tc.TestSteps);
                sb.AppendLine();
            }

            if (!string.IsNullOrWhiteSpace(tc.ExpectedResult))
            {
                sb.AppendLine("## Expected Result");
                sb.AppendLine(tc.ExpectedResult);
                sb.AppendLine();
            }

            if (!string.IsNullOrWhiteSpace(tc.ActualResult))
            {
                sb.AppendLine("## Actual Result");
                sb.AppendLine(tc.ActualResult);
                sb.AppendLine();
            }

            if (!string.IsNullOrWhiteSpace(tc.TestData))
            {
                sb.AppendLine("## Test Data");
                sb.AppendLine(tc.TestData);
                sb.AppendLine();
            }

            // Execution history
            var execList = executions?.OrderByDescending(e => e.ExecutedAt).Take(5).ToList();
            if (execList?.Count > 0)
            {
                sb.AppendLine("## Execution History");
                sb.AppendLine($"| Execution | Result | Date | Notes |");
                sb.AppendLine($"|-----------|--------|------|-------|");
                foreach (var ex in execList)
                    sb.AppendLine($"| {ex.ExecutionId} | {ex.Result} | {ex.ExecutedAt:yyyy-MM-dd HH:mm} | {ex.Notes.Replace("|", "\\|")} |");
                sb.AppendLine();
            }

            // AI analysis
            if (!string.IsNullOrWhiteSpace(aiAnalysis))
            {
                sb.AppendLine("## AI Analysis");
                sb.AppendLine(aiAnalysis);
                sb.AppendLine();
            }

            sb.AppendLine("---");
            sb.AppendLine($"*Generated by QAssistant on {DateTime.Now:yyyy-MM-dd HH:mm}*");

            return sb.ToString();
        }

