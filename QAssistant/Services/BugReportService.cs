using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using QAssistant.Models;

namespace QAssistant.Services
{
    /// <summary>
    /// Generates structured bug reports from a ProjectTask or a failed TestCase.
    /// </summary>
    public static class BugReportService
    {
        /// <summary>Generate a Markdown-formatted bug report from a task.</summary>
        public static string GenerateFromTask(ProjectTask task, string environment = "", string reporter = "")
        {
            var sb = new StringBuilder();
            sb.AppendLine($"# Bug Report: {task.Title}");
            sb.AppendLine();
            sb.AppendLine("## Summary");
            sb.AppendLine($"> {task.Title}");
            sb.AppendLine();

            if (!string.IsNullOrWhiteSpace(task.IssueIdentifier))
            {
                sb.AppendLine("## Reference");
                sb.AppendLine($"- **Issue ID:** {task.IssueIdentifier}");
                if (!string.IsNullOrWhiteSpace(task.TicketUrl))
                    sb.AppendLine($"- **Ticket URL:** {task.TicketUrl}");
                sb.AppendLine();
            }

            sb.AppendLine("## Environment");
            sb.AppendLine($"- **Environment:** {(string.IsNullOrWhiteSpace(environment) ? "Not specified" : environment)}");
            sb.AppendLine($"- **Date Reported:** {DateTime.Now:yyyy-MM-dd HH:mm}");
            if (!string.IsNullOrWhiteSpace(reporter))
                sb.AppendLine($"- **Reporter:** {reporter}");
            if (!string.IsNullOrWhiteSpace(task.Assignee))
                sb.AppendLine($"- **Assigned To:** {task.Assignee}");
            sb.AppendLine();

            sb.AppendLine("## Priority & Severity");
            sb.AppendLine($"- **Priority:** {task.Priority}");
            sb.AppendLine();

            if (!string.IsNullOrWhiteSpace(task.Description))
            {
                sb.AppendLine("## Description");
                sb.AppendLine(task.Description);
                sb.AppendLine();
            }

            sb.AppendLine("## Steps to Reproduce");
            sb.AppendLine("1. ");
            sb.AppendLine("2. ");
            sb.AppendLine("3. ");
            sb.AppendLine();

            sb.AppendLine("## Expected Result");
            sb.AppendLine("> _Describe what should happen_");
            sb.AppendLine();

            sb.AppendLine("## Actual Result");
            sb.AppendLine("> _Describe what actually happens_");
            sb.AppendLine();

            if (task.AttachmentUrls.Count > 0)
            {
                sb.AppendLine("## Attachments");
                foreach (var url in task.AttachmentUrls)
                    sb.AppendLine($"- {url}");
                sb.AppendLine();
            }

            if (!string.IsNullOrWhiteSpace(task.Labels))
            {
                sb.AppendLine("## Labels");
                sb.AppendLine(task.Labels);
                sb.AppendLine();
            }

            sb.AppendLine("---");
            sb.AppendLine($"*Generated by QAssistant on {DateTime.Now:yyyy-MM-dd HH:mm}*");

            return sb.ToString();
        }

